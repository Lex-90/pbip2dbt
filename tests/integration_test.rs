#[test]
fn determinism_two_runs_identical() {
    let tmdl = r#"table Customers
	lineageTag: c-001

	column CustomerId
		dataType: int64
		sourceColumn: CustomerId
		lineageTag: col-001

	column CustomerName
		dataType: string
		sourceColumn: CustomerName
		lineageTag: col-002

	measure 'Customer Count' =
		COUNTROWS(Customers)

	partition 'Customers' = m
		mode: import
		expression =
			let
			    Source = Sql.Database("server", "db"),
			    dbo_Customers = Source{[Schema="dbo",Item="Customers"]}[Data]
			in
			    dbo_Customers
"#;

    let (_dir, zip_path) = create_test_zip(&[("Customers.tmdl", tmdl)], None);

    let output1 = TempDir::new().expect("create output dir 1");
    let output2 = TempDir::new().expect("create output dir 2");

    let config1 = make_config(zip_path.clone(), output1.path().to_path_buf());
    let config2 = make_config(zip_path, output2.path().to_path_buf());

    pbip2dbt::run(&config1).expect("run 1");
    pbip2dbt::run(&config2).expect("run 2");

    // Compare staging SQL files
    let sql1 = std::fs::read_to_string(
        output1.path().join("models").join("staging").join("stg_customers.sql"),
    )
    .expect("read sql1");
    let sql2 = std::fs::read_to_string(
        output2.path().join("models").join("staging").join("stg_customers.sql"),
    )
    .expect("read sql2");
    assert_eq!(sql1, sql2, "Two runs should produce identical staging SQL");

    // Compare dbt_project.yml
    let proj1 = std::fs::read_to_string(output1.path().join("dbt_project.yml")).expect("read p1");
    let proj2 = std::fs::read_to_string(output2.path().join("dbt_project.yml")).expect("read p2");
    assert_eq!(proj1, proj2, "Two runs should produce identical project yml");

    // Compare schema
    let schema1 = std::fs::read_to_string(output1.path().join("models").join("_models.yml")).expect("read s1");
    let schema2 = std::fs::read_to_string(output2.path().join("models").join("_models.yml")).expect("read s2");
    assert_eq!(schema1, schema2, "Two runs should produce identical schema");
}

#[test]
fn multiple_adapters_produce_correct_syntax() {
    let tmdl = r#"table Sales
	lineageTag: s-001
	column Id
		dataType: int64
		sourceColumn: Id
		lineageTag: col-001
	partition 'Sales' = m
		mode: import
		expression =
			let
			    Source = Sql.Database("server", "db"),
			    dbo = Source{[Schema="dbo",Item="Sales"]}[Data]
			in
			    dbo
"#;

    let (_dir, zip_path) = create_test_zip(&[("Sales.tmdl", tmdl)], None);

    for adapter in &["postgres", "snowflake", "bigquery", "sqlserver"] {
        let output_dir = TempDir::new().expect("create output dir");
        let mut config = make_config(zip_path.clone(), output_dir.path().to_path_buf());
        config.adapter = adapter.to_string();

        let report = pbip2dbt::run(&config).expect(&format!("pipeline should succeed for {adapter}"));
        assert_eq!(report.adapter, *adapter);

        // Verify output exists
        assert!(
            output_dir.path().join("dbt_project.yml").exists(),
            "dbt_project.yml should exist for {adapter}"
        );
        assert!(
            output_dir.path().join("models").join("staging").join("stg_sales.sql").exists(),
            "staging SQL should exist for {adapter}"
        );

        // Verify adapter name appears in the SQL header
        let sql = std::fs::read_to_string(
            output_dir.path().join("models").join("staging").join("stg_sales.sql"),
        )
        .expect("read sql");
        assert!(
            sql.contains(&format!("Adapter: {adapter}")),
            "SQL should mention adapter: {adapter}"
        );
    }
}

