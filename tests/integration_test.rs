#[test]
fn path_traversal_rejected() {
    let dir = TempDir::new().expect("create temp dir");
    let zip_path = dir.path().join("traversal.zip");

    let file = std::fs::File::create(&zip_path).expect("create zip");
    let mut zip_writer = zip::ZipWriter::new(file);
    let options = SimpleFileOptions::default();

    // Write a definition folder first so it's found
    zip_writer
        .start_file("Test.SemanticModel/definition/model.tmdl", options)
        .expect("start model");
    zip_writer
        .write_all(b"model Model\n")
        .expect("write model");

    // Write a path traversal entry
    zip_writer
        .start_file("Test.SemanticModel/definition/../../../etc/passwd", options)
        .expect("start traversal");
    zip_writer.write_all(b"evil").expect("write evil");
    zip_writer.finish().expect("finish");

    let output_dir = TempDir::new().expect("create output dir");
    let config = make_config(zip_path, output_dir.path().to_path_buf());

    let result = pbip2dbt::run(&config);
    assert!(result.is_err(), "path traversal should be rejected");
    let err_msg = format!("{}", result.unwrap_err());
    assert!(
        err_msg.contains("E003") || err_msg.contains("traversal"),
        "error should mention path traversal: {err_msg}"
    );
}

