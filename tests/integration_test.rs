//! Integration tests for the full pbip2dbt pipeline.
//!
//! These tests create in-memory ZIP fixtures that mimic a PBIP structure,
//! run the full pipeline, and verify the generated dbt project.

use pbip2dbt::config::Config;
use std::io::Write;
use std::path::PathBuf;
use tempfile::TempDir;
use zip::write::SimpleFileOptions;

/// Create a minimal PBIP zip in a temp directory.
/// Returns the path to the zip file.
fn create_test_zip(tables_tmdl: &[(&str, &str)], relationships_tmdl: Option<&str>) -> (TempDir, PathBuf) {
    let dir = TempDir::new().expect("create temp dir");
    let zip_path = dir.path().join("test_model.zip");

    let file = std::fs::File::create(&zip_path).expect("create zip file");
    let mut zip_writer = zip::ZipWriter::new(file);
    let options = SimpleFileOptions::default();

    // Write model.tmdl
    let model_tmdl = "model Model\n\tsource = powerBi\n";
    zip_writer
        .start_file("TestModel.SemanticModel/definition/model.tmdl", options)
        .expect("start model.tmdl");
    zip_writer
        .write_all(model_tmdl.as_bytes())
        .expect("write model.tmdl");

    // Write table files
    for (filename, content) in tables_tmdl {
        let path = format!(
            "TestModel.SemanticModel/definition/tables/{filename}"
        );
        zip_writer.start_file(&path, options).expect("start table file");
        zip_writer
            .write_all(content.as_bytes())
            .expect("write table file");
    }

    // Write relationships if provided
    if let Some(rels) = relationships_tmdl {
        zip_writer
            .start_file(
                "TestModel.SemanticModel/definition/relationships.tmdl",
                options,
            )
            .expect("start relationships");
        zip_writer
            .write_all(rels.as_bytes())
            .expect("write relationships");
    }

    zip_writer.finish().expect("finish zip");

    (dir, zip_path)
}

fn make_config(input: PathBuf, output: PathBuf) -> Config {
    Config {
        input,
        output,
        adapter: "postgres".to_string(),
        project_name: "test_project".to_string(),
        source_name: Some("test_source".to_string()),
        schema: "raw".to_string(),
        materialization_default: "view".to_string(),
        skip_measures: false,
        skip_calculated_tables: false,
        skip_calculated_columns: false,
        skip_tests: false,
        confidence_threshold: 0.0,
        verbose: false,
        dry_run: false,
    }
}

#[test]
fn simple_import_table() {
    let tmdl = r#"table Sales
	lineageTag: abcd-1234

	column OrderId
		dataType: int64
		sourceColumn: OrderId
		lineageTag: col-001

	column Amount
		dataType: double
		sourceColumn: Amount
		lineageTag: col-002

	partition 'Sales' = m
		mode: import
		expression =
			let
			    Source = Sql.Database("localhost", "adventureworks"),
			    dbo_Sales = Source{[Schema="dbo",Item="Sales"]}[Data]
			in
			    dbo_Sales
"#;

    let (_dir, zip_path) = create_test_zip(&[("Sales.tmdl", tmdl)], None);
    let output_dir = TempDir::new().expect("create output dir");
    let config = make_config(zip_path, output_dir.path().to_path_buf());

    let report = pbip2dbt::run(&config).expect("pipeline should succeed");

    // Verify report
    assert_eq!(report.summary.tables_total, 1);
    assert_eq!(report.adapter, "postgres");

    // Verify generated files
    assert!(output_dir.path().join("dbt_project.yml").exists());
    assert!(output_dir.path().join("packages.yml").exists());
    assert!(output_dir.path().join("models").join("staging").join("stg_sales.sql").exists());
    assert!(output_dir.path().join("models").join("_models.yml").exists());
    assert!(output_dir.path().join("translation_report.json").exists());

    // Verify staging SQL content
    let sql = std::fs::read_to_string(
        output_dir.path().join("models").join("staging").join("stg_sales.sql"),
    )
    .expect("read staging sql");
    assert!(sql.contains("source('test_source', 'sales')"));
    assert!(sql.contains("Auto-generated by pbip2dbt"));
    assert!(sql.contains("select"));
    assert!(sql.contains("from source"));

    // Verify dbt_project.yml
    let project = std::fs::read_to_string(output_dir.path().join("dbt_project.yml"))
        .expect("read project yml");
    assert!(project.contains("name: 'test_project'"));
    assert!(project.contains("materialized: view"));
}

